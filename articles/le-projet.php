    <h4 id="manifestation--futur-en-seine" class="">Manifestation – Futur en Seine</h4>

    <p>L’atelier des chercheurs sera à Futur en Seine du 12 au 15 juin 2014. De plus, une table ronde sur la question des environnements d’apprentissage et des méthodes pédagogiques, avec différents acteurs du monde du design se tiendra le 16 juin à 18h à l’EnsAD. N’hésitez pas à vous joindre à nous.</p>

